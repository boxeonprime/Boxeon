                <h1 class="font-size-3-em center primary-color">Nigerian Egusi Soup</h1>

                <p>Egusi (also known by variations including egwusi, agusi, ohue, Ikpan, Ikon, and agushi) is the name for
                    the
                    protein-rich seeds of certain cucurbitaceous plants (squash, melon, gourd), which after being dried and
                    ground are used as a major ingredient in West African cuisine.</p>
                <p>Egusi is used as a soup thickener, with considerable local variation. Besides the seeds, water, and oil,
                    egusi soup typically contains leaf vegetables, palm oil, other vegetables, seasonings, and meat. Leaf
                    vegetables typically used for egusi soup include bitter leaf, pumpkin leaf, celosia, and spinach. Typical
                    other vegetables include tomatoes and okra. Typical seasonings include chili peppers, onions, and locust
                    beans. Also commonly used are beef, goat, fish, shrimp, or crayfish.</p>
                <p>In Nigeria, egusi is common among the people of the southwestern Yoruba people, Efik, Ibibio, and Annang
                    people of south-south Nigeria, and the southeastern part of Nigeria by the Igbo people of southern
                    Nigeria.</p>
                <p> In Ghana, egusi is also called akatoa or agushi, and as in Nigeria is used for soup and stew, and
                    most popularly in palaver sauce.
                </p>
