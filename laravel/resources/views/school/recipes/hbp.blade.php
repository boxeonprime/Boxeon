By Trevor Prime

Published September 21 2022


Nigerian beans porridge, also known as stewed beans, is a dietary staple in many households in the country. It is called
“ewa riro” in Yoruba or sometimes just referred to as “ewa”, which means beans. It is similar to the Ghanaian “red red”
and is perfect for vegetarians.
Beans are a staple food all throughout Africa because they are versatile, packed with protein, fiber, and full of
flavor. It’s very common to find street vendors selling beans at the exit of most schools in Cameroon in the morning
with puff-puff. You can also find them outside of the food markets in many cities and at most main plazas every day of
the week. It is considered a breakfast or main meal depending on how it is prepared.


