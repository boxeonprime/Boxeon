Fufu (or fufuo, foofoo, foufou) is a
dough-like food found in West African cuisine. In addition to
Ghana, it is also found in Sierra Leone, Guinea, Liberia, Cote D'Ivoire, Benin, Togo, Nigeria,
Cameroon, the Democratic Republic of Congo, the Central African Republic, the Republic of Congo,
Angola and Gabon.

It is often made in the traditional Ghanaian, Ivorian, Liberian, and Cuban method of separately
mixing and pounding equal portions of boiled cassava with green plantain or cocoyam, or by mixing
cassava/plantains or cocoyam flour with water and stirring it on a stove. A modern method of making
fufu is to chop then blend the raw cassava and other ingredients then cook on a stove in water. The
viscosity is then adjusted based on personal preference and eaten with broth-like soups.
Some countries, particularly Nigeria, have a version of fufu made from fermented Cassava dough
(called akpu by Nigerians) that is eaten with thick textured stews. Other flours, such as semolina,
maize flour, or mashed plantains may take the place of cassava flour.

Fufu is traditionally eaten with the fingers, and a small ball of it can be dipped into an
accompanying soup or sauce.


