Jollof Rice is the most popular rice dish
from West Africa. The dish is typically made
with
long-grain rice, tomatoes, onions, spices, vegetables and meat in a single pot, although its
ingredients
and preparation methods vary across different regions.
Jollof originates from the Senegambian region that was ruled by the Wolof or Jolof Empire in the 14th
century, spanning parts of today's Senegal, The Gambia and Mauritania, where rice was grown. The
dish
has its roots in a traditional dish called thieboudienne, containing rice, fish, shellfish and
vegetables.
Jollof Rice traditionally consists of rice, cooking oil, vegetables such as tomato, onion, red
pepper,
garlic, ginger and scotch bonnet chilli peppers. To enhance the colour of the dish, tomato paste
(pur√©e)
is added. As seasoning, spices, salt, stock cubes (a blend of flavour enhancers, salt, nutmeg and
herbs), curry powder and dried thyme are used. To complement the dish, chicken, turkey, beef or fish
are
often served with the dish.
Each West African country has at least one variant form of the dish, with Ghana, Nigeria, Sierra
Leone,
Liberia and Cameroon particularly competitive as to which country makes the best jollof. This is
especially prominent between Nigeria and Ghana, in a rivalry dubbed the "Jollof Wars".




