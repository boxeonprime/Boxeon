Okra soup (African okro soup) is made with
okra vegetables cooked in a delicious mixture of palm oil, shrimps, goat meat, fish, and African
spices and simmered to perfection!
Okra Soup is one of the quickest and easiest Nigerian soups to prepare. Some argue that a lot of work
goes to the cutting of the two vegetables used in preparing this recipe. Yes but once the vegetables
are ready; it cooks in no time at all.

