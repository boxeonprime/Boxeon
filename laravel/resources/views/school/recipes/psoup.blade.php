Simple Nigerian Pepper Soup Recipe: 7 Easy Steps
By Trevor Prime Published September 21 2022



Nigerian Pepper Soup is a one-pot dish that's defined by the nutty, bitter, woodsy, and floral notes
imparted by a mix of spices and herbs.
Pepper soup has a light, watery texture. The name is a misnomer because the soup is not necessarily
defined by a pepper-forward flavor profile, that is, the flavors are much more complex, with nutty,
bitter, woodsy, and floral notes, as well as warmth. It is considered to be a delicacy in West
Africa, and is traditionally prepared with some medicinal herbs.

